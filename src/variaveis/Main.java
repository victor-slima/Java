public class Main {
    public static void main(String[] args) {
        // Variáveis são visíveis apenas em escopos.    
        // Declaração de variáveis:
        int hora;
        String nome;
        double salario;

        
        
        // Atribuição:
        hora = 13;
        nome = "Victor";
        salario = 3909.98;

        // Inicialização de variáveis:
        System.out.println(hora);
        System.out.println(nome);
        System.out.println(salario);
    }
        
}
